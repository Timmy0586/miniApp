    userInfo: {},
    hasUserInfo: false,
    canIUse: wx.canIUse('button.open-type.getUserInfo'),
    eta: date.getFullYear() + "-" + (date.getMonth() + 1) + "-" + date.getDate() + " " + currentHours + ":" + currentMinute,
    startDate: "请选择日期",
